$issuedLabel = $issuedAt !== '' ? format_datetime($issuedAt) : '';
$visitDate = format_appt_date($content['consultation_date'] ?? date('Y-m-d'));
$doctorName = trim((string) ($content['doctor_name'] ?? ''));
$issuedBy = trim((string) ($content['issued_by_name'] ?? $document['issued_by_display_name'] ?? $document['issued_by_username'] ?? 'Staff'));
$purpose = trim((string) ($content['purpose'] ?? ''));
$restDays = $content['rest_days'] ?? null;
$remarks = trim((string) ($content['remarks'] ?? ''));
$clinicalNotes = trim((string) ($content['clinical_notes'] ?? ''));

$basePath = app_base_path();
$logoPath = '/assets/bhs_logo_v3.png';
$uLogo = ($basePath === '' ? '' : $basePath) . $logoPath;
$root = defined('BHC_ROOT') ? BHC_ROOT : dirname(__DIR__, 2);
