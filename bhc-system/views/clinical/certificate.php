      <td><strong><?= h($content['full_name'] ?? '') ?></strong></td>
    </tr>
    <tr>
      <td>BHC ID</td>
      <td><?= h($content['bhc_id'] ?? '') ?></td>
    </tr>
    <tr>
      <td>Visit date</td>
      <td><?= h($visitDate) ?></td>
    </tr>
    <tr>
      <td>Issued on</td>
