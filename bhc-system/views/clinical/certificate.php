      <tr>
        <td>Recommended rest</td>
        <td><?= h((string) $restDays) ?> day<?= (int) $restDays === 1 ? '' : 's' ?></td>
      </tr>
    <?php endif; ?>
    <?php if ($clinicalNotes !== ''): ?>
      <tr>
        <td>Clinical notes</td>
        <td><?= nl2br(h($clinicalNotes)) ?></td>
      </tr>
    <?php endif; ?>
    <?php if ($remarks !== ''): ?>
      <tr>
        <td>Remarks</td>
        <td><?= nl2br(h($remarks)) ?></td>
      </tr>
    <?php endif; ?>
  </table>

